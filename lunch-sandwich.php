    <figure>
        <img class="turkey-wrap" src="images/turkey-wrap.jpg" alt="a turkey havarti wrap">
        <figcaption>
            Jennifer Nigro
        </figcaption>
    </figure>

    <h3>Ingredients</h3>
    <ul class="ingredients">
        <li>1 Tortilla wrap (sundried tomato and basil recommended)</li>
        <li>2 oz of Chipotle bean spread (or chipotle mayo)</li>
        <li>4 oz of Sliced smoked turkey</li>
        <li>2 Havarti cheese slices</li>
        <li>1 oz of Baby spinach</li>
    </ul>

    <h3>Instructions</h3>
    <ol class="instructions">
        <li class="directions">Lay tortilla flat and evenly spread white bean spread (or mayo) over the tortilla leaving a 2 inch border.</li>
        <li class="directions">Evenly lay the turkey, cheese and spinach over the spread in that order.</li>
        <li class="directions">Slightly fold two sides over and roll the tortilla over to close the wrap.</li>
    </ol>
</article>
