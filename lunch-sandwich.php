    <h2>Pesto Chicken Wrap</h2>

    <figure>
        <img class="chicken-wrap" src="images/chicken-wrap.jpg" alt="a pesto chicken wrap">
        <figcaption>
            Jennifer Nigro
        </figcaption>
    </figure>

    <h3>Ingredients</h3>
    <ul class="ingredients">
        <li>1 Tortilla Wrap</li>
        <li>2 oz of Pesto</li>
        <li>1 packet of Mayonnaise</li>
        <li>2 slices of Mozzarella Cheese</li>
        <li>2 oz of Roasted red cherry tomatoes</li>
        <li>5 oz of Sous vide cooked chicken</li>
    </ul>

    <h3>Instructions</h3>
    <ol class="instructions">
        <li class="directions">Lay tortilla flat and evenly spread the pesto and mayonnaise over the tortilla leaving a 2 inch border</li>
        <li class="directions">Lay the cheese and tomatoes. Hand tear the chicken into pieces and lay over the tomatoes.</li>
        <li class="directions">Slightly fold two sides over and roll the tortilla to close the wrap.</li>
    </ol>
</article>

<article class='recipe-title'>
