    <h3>Ingredients</h3>
    <ul class="ingredients">
        <li>1 package 16 hamburger buns</li>
        <li>1 lb ground chuck</li>
        <li>1 Vidalia Onion</li>
        <li>Beef broth</li>
        <li>8 slices mild cheddar or American cheese</li>
        <li>16 hamburger dill slices</li>
        <li>Salt</li>
        <li>Pepepr</li>            
    </ul>
    
    <h3>Instructions</h3>
    <ol class="instructions">
        <li class="directions">Chop the Vidalia Onion</li>
        <li class="directions">Pour about a quarter inch of beef broth into a large pan and add the onions. Add salt and pepper to taste and cook at medium heat until the broth boils over</li>
        <li class="directions">Press and spread the chuck until it's a large rectangle, around 10x15 inches</li>
        <li class="directions">Cut the meat into 16 equal rectangles and poke 5 holes in each patty. Sprinkle salt and pepper to season</li>
        <li class="directions">Heat a griddle to 400<sup>o</sup>F and spread the onions. Put the patties on the griddle and cook until about halfway done, then flip them</li>
        <li class="directions">After the patties are flipped, add cheese and let them cook until the cheese it fully melted and the meat is thoroughly cooked.</li>
        <li class="directions">Cut the sides off the buns and place a dill pickle slice on the bottom bun. Top with a patty, condiments (optional), and the top bun.</li>
        <li class="directions">Enjoy!</li>        
    </ol>       
