    <h3>Ingredients</h3>
    <ul class="ingredients">
        <li>1 box Funfetti cake mix (or any cake mix of your choosing)</li>
        <li>4 large eggs</li>
        <li>2/3 cup melted butter</li>
        <li>1 cup milk </li>
        <li>2 cups frosting</li>
    </ul>

    <h3>Instructions</h3>
    <ol class="instructions">
        <li class="directions">Replace water 1:1 with milk (ideally whole milk, but you can use any kind you want)</li>
        <li class="directions">Replace oil with melted butter and double the amount (1/2 cup becomes 1 cup, etc.)</li>
        <li class="directions">Add an extra egg (or two if you want the cupcakes to be very rich, or if you’re using medium eggs)</li>
        <li class="directions">Preheat oven to 350°F. Add cake mix, eggs, melted butter, and milk. Mix on low for 2 minutes.</li>
        <li class="directions">Scoop batter into a lined cupcake tin so the cups are 3/4 full.</li>
        <li class="directions">Bake for 20 minutes or until a toothpick is inserted and comes out clean. When the cupcakes are completely cook, frost and decorate.</li>
    </ol>
