<h2>French Toast</h2>

<figure>
    <img class="" src="images/FILENAME.JPG" alt="">
    <figcaption>
        French Toast served with strawberries, blueberries, blackberries, watermelon, and mint leave. (Taken by Jennifer Nigro)
    </figcaption>
