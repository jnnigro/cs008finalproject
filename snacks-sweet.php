<article class='recipe-title'>
    <h2>Rainbow Smoothie Pops</h2>

    <figure>
        <img class="smoothie-pops" src="images/rainbow-pops.jpg" alt="rainbow smoothie pops surrounded by fruit">
        <figcaption>
            Jennifer Nigro
        </figcaption>
    </figure>

    <h3>Ingredients</h3>
    <ul class="ingredients">
        <li>2 1/2 cups plain or vanilla yogurt divided</li>
        <li>1/2 cup strawberries</li>
        <li>1/2 cup raspberries</li>
        <li>1 cup oranges</li>
        <li>1 cup pineapple</li>
        <li>1 cup bananas</li>
        <li>1 cup spinach</li>
        <li>1 cup mixed berries</li>
    </ul>

    <h3>Instructions</h3>
    <ol class="instructions">
        <li class="directions">For the red layer: Add 1/2 cup yogurt, strawberries, and raspberries to a blender and blend until smooth. If you don't have a powerful blender and are using frozen fruit, chop them into smaller pieces before blending. Transfer to a cup and refrigerate.</li>
        <li class="directions">For the orange layer: Add 1/2 cup yogurt and chopped oranges to a blender and blend until smooth. Transfer to a cup and refrigerate.</li>
        <li class="directions">For the yellow layer: Add 1/2 cup yogurt and chopped pineapple to a blender and blend until smooth. Transfer to a cup and refrigerate.</li>
        <li class="directions">For the green layer: Add 1/2 cup yogurt, bananas, and spinach to a blender and blend until smooth. Transfer to a cup and refrigerate.</li>
        <li class="directions">For the purple layer: Add 1/2 cup yogurt and mixed berries (I used a mix of raspberries, blackberries, and blueberries) to a blender and blend until smooth. Transfer to a cup and refrigerate.</li>
        <li class="directions">To assemble, spoon each color into each popsicle mold. I started with red and finished with purple, but you can go in any order. If you have molds with reusable caps with sticks, put them on and freeze the popsicles for 5 hours. If you're using wooden popsicle sticks, freeze for 1-2 hours, add the stick, and freeze for the remaining 3-4 hours.</li>
        <li class="directions">After 5 hours, run the popsicle mold under warm water and carefully remove the popsicle. Enjoy!</li>
    </ol>
</article>

