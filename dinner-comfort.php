    <h3>Ingredients</h3>
    <ul class="ingredients">
        <li>2 tablespoons Sultan brand olive oil</li>
        <li>2 lbs ground lamb</li>
        <li>1 tablespoon Ziyad brand cumin</li>
        <li>1 tablespoon Ziyad brand oregano</li>
        <li>1 tablespoon basil</li>
        <li>1 onion diced</li>
        <li>3 cloves minced garlic</li>
        <li>28oz can crushed tomatoes</li>
        <li>2 tablespoons Ziyad brand tomato paste</li>
        <li>1 jar pimentos</li>
        <li>1.5 lbs spaghetti</li>
        <li>Salt</li>
        <li>1 1/4 cup breadcrumbs</li>
        <li>1 1/2 cup grated Parmesan cheese</li>
        <li>Sumac to finish</li>
    </ul>
    
    <h3>Instructions</h3>
    <ol class="instructions">
        <li class="directions">Add 2 tablespoons of olive oil to a large skillet with raised sides. When it's hot, add 2 lbs of ground lamb and break it up into smaller pieces. Season with 1 tablespoon cumin, 1 tablespoon oregano, 1 tablespoon basil, and 2 tablespoons parsley. Mix it to ensure everything is combined.</li>
        <li class="directions">Add 1 diced onion and 3 cloves minced garlic and stir, cooking until the onions have softened.</li>
        <li class="directions">Add a 28 oz can of crushed tomatoes and 2 tablespoons tomato paste and mix everything together. Add a small jar of pimientos and stir. Simmer for 20 minutes.</li>
        <li class="directions">Preheat the oven to 350°F. Cook 1 1/2 lbs of spaghetti according to the instructions on the box, cooking until the pasta is al dente. Strain.</li>
        <li class="directions">Add the spaghetti to a large baking dish. Add the sauce and spread it out to cover the spaghetti. Top with 1/4 cup of breadcrumbs and 1/2 cup (or more) of Parmesan cheese. Bake at 350°F for 20 minutes.</li>
        <li class="directions">Plate a portion of the spaghetti and sprinkle with sumac. Enjoy!</li>            
    </ol>                  
