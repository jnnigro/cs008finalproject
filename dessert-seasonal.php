<article class="recipe-title">
    <h2>Chocolate Cupcakes with Coconut Grass and Chocolate Covered Strawberry Footballs</h2>

    <figure>
        <img class="footballcupcakes" src="images/football-cupcakes.JPG" alt="Chocolate Cupcakes with Coconut Grass and Chocolate Covered Strawberry Footballs">
        <figcaption>
            Jennifer Nigro
        </figcaption>
    </figure>

    <h3>Ingredients</h3>
    <ul class="ingredients">
        <li>100g all-purpose flour</li>
        <li>20g cocoa powder</li>
        <li>140g sugar</li>
        <li>1 1/2 teaspoon baking powder</li>
        <li>pinch salt</li>
        <li>40g unsalted butter at room temperature</li>
        <li>120ml milk</li>
        <li>1 egg</li>
        <li>1/4 tsp vanilla extract</li>
        <li>1 stick butter</li>
        <li>1 1/2 cup sifted powdered sugar</li>
        <li>1 tsp vanilla extract</li>
        <li>2 tbsp milk</li>
        <li>12 strawberries</li>
        <li>2/3 cup chocolate chips</li>
        <li>1/2 cup white chocolate chips</li>
        <li>2/3 cup shredded coconut</li>
        <li>Green food dye</li>
    </ul>

    <h3>Instructions</h3>
    <ol class="instructions">
        <li class="directions">Melt the chocolate chips in the microwave at 30 second intervals until smooth. Dip the strawberries in chocolate for a smooth, even coat. Place on a baking tray lined with parchment paper and chill for 15 minutes.</li>
        <li class="directions">Melt the white chocolate chips in the same fashion and transfer to a piping bag. Snip the end and pipe laces on the strawberry “footballs.” Chill until it’s time to top the cupcakes.</li>
        <li class="directions">Preheat the oven to 170°C or 325°F. Add the flour, cocoa powder, sugar, baking powder, salt and butter in a mixing bowl. Beat until everything is combined and has a sandy texture.</li>
        <li class="directions">Whisk the milk, egg and vanilla extract together in a separate bowl, then slowly pour it into the dry ingredients and beat until everything is combined. Go in with a spatula to get anything at the bottom not mixed in and beat for another 20 seconds.</li>
        <li class="directions">Spoon the batter into a lined cupcake tin until 2/3 full. Bake for 20-25 minutes until a toothpick inserted in the center comes out clean. Let the cupcakes cool slightly in the tin and transfer them to a wire rack to cool completely.</li>
        <li class="directions">While the cupcakes are baking, add the shredded coconut to a bowl and add a few drops of green food dye. Stir the coconut until it’s all green.</li>
        <li class="directions">While the cupcakes are cooling, prepare the frosting. Add the butter to a mixing bowl and beat it until it’s creamy. Reduce the speed of the mixer and slowly add the sifted powdered sugar. Once it looks more like frosting, add the vanilla. Gradually add the milk until the frosting is a desired texture.</li>
        <li class="directions">When the cupcakes are completely cool, frost the cupcakes. You could do this with a piping bag or just with a palette knife. Immediately after frosting, dunk the cupcake frosting-side down into the coconut “grass.”</li>
        <li class="directions">Top with a chocolate covered strawberry “football” and enjoy!</li>
    </ol>
</article>

