    <figure>
        <img class="pumpkin-pie" src="images/pumpkin-pie.jpg" alt="pumpkin pie">
        <figcaption>
            Jennifer Nigro
        </figcaption>
    </figure>

    <h3>Ingredients</h3>
    <ul class="ingredients">
        <li>4 eggs</li>
        <li>29oz pumpkin purée</li>
        <li>1 1/2 cups sugar</li>
        <li>1 tsp salt</li>
        <li>2 tsp cinnamon</li>
        <li>1 tsp ginger</li>
        <li>1 tsp allspice</li>
        <li>2 cups milk</li>
        <li>2 pie crusts</li>
    </ul>

    <h3>Instructions</h3>
    <ol class="instructions">
        <li class="directions">Preheat oven to 400°F. Mix beaten eggs, pumpkin, sugar, salt, and spices until well combined. Add milk and stir until mixture is smooth.</li>
        <li class="directions">Line two 9-inch pie pans with pie crust or use pre-made crusts. Pur the filling into the pastry shells, dividing it equally.</li>
        <li class="directions">Bake 45 minutes or until a knife inserted halfway between center and edge comes out clean.</li>
    </ol>
