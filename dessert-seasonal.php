<article class='recipe-title'>
    <h2>Pumpkin Roll</h2>

    <figure>
        <img class="pumpkin-roll" src="images/pumpkin-roll.jpg" alt="sliced pumpkin roll on a plate next to an unsliced roll">
        <figcaption>
            Jennifer Nigro
        </figcaption>
    </figure>

    <h3>Ingredients</h3>
    <ul class="ingredients">
        <li>Powdered Sugar</li>
        <li>3/4 cup all purpose flour</li>
        <li>1/2 tsp baking soda</li>
        <li>1 tsp ground cinnamon</li>
        <li>1/4 tsp salt</li>
        <li>3 large eggs</li>
        <li>1 cup sugar</li>
        <li>2/3 cup pumpkin purée</li>
        <li>1 pkg (8oz.) cream cheese, softened</li>
        <li>1 cup sifted powdered sugar</li>
        <li>6 tbsp butter, softened</li>
        <li>1 tsp vanilla extract</li>
    </ul>

    <h3>Instructions</h3>
    <ol class="instructions">
        <li class="directions">For the Cake: Preheat oven to 375°F. Grease a 15x10 inch jelly-roll pan and line with wax paper. Grease and flour the paper. Sprinkle a thin, cotton, kitchen towel with powdered sugar.</li>
        <li class="directions">Combine flour, baking powder, baking soda, cinnamon, and salt in a small bowl. Beat eggs and sugar in a large mixer bowl until thick. Beat in pumpkin. Stir in the flour mixture. Spread evenly into the prepared pan.</li>
        <li class="directions">Bake for 13-15 minutes or until top of cake springs back when touched. Immediately loosen and turn the cake onto the prepared towel. Carefully peel off the wax paper. Roll up the cake and towel together, starting with the narrow end. Cool on a wire rack.</li>
        <li class="directions">For the Filling: Beat cream cheese, powdered sugar, butter, and vanilla extract in a small mixer bowl until smooth. Carefully unroll the cake and remove the towel. Spread the cream cheese mixture over the cake and reroll the cake. Wrap in plastic wrap and refrigerate at least one hour.</li>
    </ol>
</article>

