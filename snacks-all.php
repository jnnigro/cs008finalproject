<article class='recipe-title'>
    <h2>Mickey Mouse Soft Pretzels</h2>

    <figure>
        <img class="mickey-pretzels" src="images/mickey-pretzels.jpg" alt="soft pretzels shaped like mickey mouse">
        <figcaption>
            Jennifer Nigro
        </figcaption>
    </figure>

    <h3>Ingredients</h3>
    <ul class="ingredients">
        <li>1 package Auntie Anne's Pretzels & More Homemade Baking Mix</li>
        <li>Water</li>
        <li>Flour for kneading</li>
        <li>Melted butter (optional)</li>
    </ul>

    <h3>Instructions</h3>
    <ol class="instructions">
        <li class="directions">Dissolve yeast in 1 1/4 cups warm water and let it sit for 2 minutes.</li>
        <li class="directions">Add dough mix and mix until a ball forms.</li>
        <li class="directions">Knead the dough ball on a lightly floured surface for 5-7 minutes. Add a small amount of flour if the dough is too sticky.</li>
        <li class="directions">Place dough ball in large greased bowl and cover. Let it rise for 30 minutes in a warm spot. While it's rising, preheat the oven to 425°F.</li>
        <li class="directions">Turn out the dough and cut it into strips. Take a strip and roll, stretch, and pull the dough into a rope. Then shape it into a circle (Mickey's face). Take two smaller pieces, make ropes, and shape them into smaller circles to make Mickey's ears. Place them on the larger circle and press on them to make sure they're attached.</li>
        <li class="directions">Dissolve the baking soda into 4 cups of very hot water.</li>
        <li class="directions">Dunk the pretzels in the baking soda solution, then place them on a greased baking sheet.</li>
        <li class="directions">Add salt. If you want to make cinnamon sugar Mickey pretzels, skip this step.</li>
        <li class="directions">Bake pretzels for 5 minutes, then, turn the tray 180° and bake for another 2-5 minutes until the pretzels are golden brown.</li>
        <li class="directions">For salted pretzels, brush melted butter onto the pretzels. For cinnamon sugar pretzels, brush pretzels with melted butter, then sprinkle on the cinnamon sugar. Enjoy!</li>
    </ol>
</article>

<article class='recipe-title'>
    <h2>Peanut Butter and Pumpkin Dog Biscuits</h2>

    <figure>
        <img class="dog-biscuits" src="images/dog-biscuits.jpg" alt="top left: round, star, and flower shaped biscuits. top right: biscuit in front of dog. bottom left: dog biting for biscuit. bottom right: dog eating biscuit.">
        <figcaption>
            Jennifer Nigro
        </figcaption>
    </figure>

    <h3>Ingredients</h3>
    <ul class="ingredients">
        <li>2/3 cup pumpkin puree</li>
        <li>1/4 cup peanut butter</li>
        <li>2 large eggs</li>
        <li>3 cups flour</li>
    </ul>

    <h3>Instructions</h3>
    <ol class="instructions">
        <li class="directions">Preheat oven to 350°F. Line a baking sheet with parchment paper and set aside.</li>
        <li class="directions">Beat pumpkin puree, peanut butter and eggs on medium-high until well combined, about 1-2 minutes. Gradually add 2 1/2 cups flour at low speed, beating just until incorporated. Add an additional 1/4 cup flour at a time just until the dough is no longer sticky.</li>
        <li class="directions">On a lightly floured surface, knead the dough a few times until it comes together. Using a rolling pin, roll the dough to 1/4-inch thickness. Using cookie cutters, cut out desired shapes and place onto the baking sheet.</li>
        <li class="directions">Bake 20-25 minutes until the edges are golden brown.</li>
        <li class="directions">Let cool completely and enjoy a snack with your dog!</li>
    </ol>
</article>

<article class='recipe-title'>
    <h2>Rainbow Smoothie Pops</h2>

    <figure>
        <img class="smoothie-pops" src="images/rainbow-pops.jpg" alt="rainbow smoothie pops surrounded by fruit">
        <figcaption>
            Jennifer Nigro
        </figcaption>
    </figure>

    <h3>Ingredients</h3>
    <ul class="ingredients">
        <li>2 1/2 cups plain or vanilla yogurt divided</li>
        <li>1/2 cup strawberries</li>
        <li>1/2 cup raspberries</li>
        <li>1 cup oranges</li>
        <li>1 cup pineapple</li>
        <li>1 cup bananas</li>
        <li>1 cup spinach</li>
        <li>1 cup mixed berries</li>
    </ul>

    <h3>Instructions</h3>
    <ol class="instructions">
        <li class="directions">For the red layer: Add 1/2 cup yogurt, strawberries, and raspberries to a blender and blend until smooth. If you don't have a powerful blender and are using frozen fruit, chop them into smaller pieces before blending. Transfer to a cup and refrigerate.</li>
        <li class="directions">For the orange layer: Add 1/2 cup yogurt and chopped oranges to a blender and blend until smooth. Transfer to a cup and refrigerate.</li>
        <li class="directions">For the yellow layer: Add 1/2 cup yogurt and chopped pineapple to a blender and blend until smooth. Transfer to a cup and refrigerate.</li>
        <li class="directions">For the green layer: Add 1/2 cup yogurt, bananas, and spinach to a blender and blend until smooth. Transfer to a cup and refrigerate.</li>
        <li class="directions">For the purple layer: Add 1/2 cup yogurt and mixed berries (I used a mix of raspberries, blackberries, and blueberries) to a blender and blend until smooth. Transfer to a cup and refrigerate.</li>
        <li class="directions">To assemble, spoon each color into each popsicle mold. I started with red and finished with purple, but you can go in any order. If you have molds with reusable caps with sticks, put them on and freeze the popsicles for 5 hours. If you're using wooden popsicle sticks, freeze for 1-2 hours, add the stick, and freeze for the remaining 3-4 hours.</li>
        <li class="directions">After 5 hours, run the popsicle mold under warm water and carefully remove the popsicle. Enjoy!</li>
    </ol>
</article>

